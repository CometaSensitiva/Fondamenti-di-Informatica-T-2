package esempio01;

/**
 * 
 * The Rettangolo interface represents a rectangle shape with height and base
 * dimensions.
 * 
 * @author michelemazzaa
 */
public interface Rettangolo {

	/**
	 * 
	 * Returns the height dimension of this rectangle.
	 * 
	 * @return the height of this rectangle
	 */
	public double altezza();

	/**
	 * 
	 * Returns the base dimension of this rectangle.
	 * 
	 * @return the base of this rectangle
	 */
	public double base();
}
