package esempio02;

/**
 * 
 * The Rettangolare interface represents a rectangular shape. It specifies
 * methods for retrieving the width and length of the rectangle.
 * 
 * @author michelemazzaa
 */
public interface Rettangolare {

	/**
	 * 
	 * Returns the width of the rectangular shape.
	 * 
	 * @return the width of the rectangle as a double.
	 */
	public double larghezza();

	/**
	 * 
	 * Returns the length of the rectangular shape.
	 * 
	 * @return the length of the rectangle as a double.
	 */
	public double lunghezza();
}
