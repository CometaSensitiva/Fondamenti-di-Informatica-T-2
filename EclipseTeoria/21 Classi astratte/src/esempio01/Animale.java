
package esempio01;

/**
 * An abstract class representing an animal.
 * 
 * @author michelemazzaa
 */
public abstract class Animale {

	/**
	 * Returns a String representing how the animal moves.
	 * 
	 * @return a String representing how the animal moves.
	 */
	public abstract String siMuove();

	/**
	 * Returns a String representing where the animal lives.
	 * 
	 * @return a String representing where the animal lives.
	 */
	public abstract String vive();

}
