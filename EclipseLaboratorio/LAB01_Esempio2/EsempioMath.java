/**
 * This is an example class that demonstrates the use of the Math class.
 * 
 * @author michelemazzaa
 */
public class EsempioMath {

	/**
	 * This is the main method that calculates the sine of PI/3 and prints the
	 * result.
	 * 
	 * @param args An array of strings representing the command line arguments.
	 */
	public static void main(String args[]) {
		double x = Math.sin(Math.PI / 3);
		System.out.println(x);
	}
}